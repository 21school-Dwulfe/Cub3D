#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (!dst && !src)
		return (NULL);
	while (n--)
	{
		*d++ = *s++;
		if (*(d - 1) == (unsigned char)c)
			return (d);
	}
	return ((void *)0);
}
